    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    String user = username.getText();
    String pass = new String(password.getPassword());

    if (user.isEmpty() || pass.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter username and password.");
        return;
    }

    try {
        Connect(); // Ensure this method initializes 'con'

        pst = con.prepareStatement("SELECT program, yrsec, account_type FROM register WHERE username=? AND password=?");
        pst.setString(1, user);
        pst.setString(2, pass);
        rs = pst.executeQuery();

        if (rs.next()) {
            String program = rs.getString("program");
            String yrsec = rs.getString("yrsec");
            String accountType = rs.getString("account_type");

            JOptionPane.showMessageDialog(this, "Login Successful!");

            if ("admin".equalsIgnoreCase(accountType)) {
                admin adminPage = new admin();
                adminPage.setVisible(true);
                this.dispose();
            } else if ("student".equalsIgnoreCase(accountType)) {
                WelcomeUser studentPage = new WelcomeUser(user, program, yrsec);
                studentPage.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Unknown account type.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Invalid username or password.");
        }

    } catch (SQLException ex) {
        Logger.getLogger(LoginPage.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "An error occurred while processing your request. Please try again later.");
    } finally {
        // Clean up resources
        try {
            if (rs != null) rs.close();
            if (pst != null) pst.close();
            if (con != null) con.close();
        } catch (SQLException e) {
            Logger.getLogger(LoginPage.class.getName()).log(Level.SEVERE, null, e);
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

